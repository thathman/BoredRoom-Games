#!/usr/bin/env node
// Keep the installable runtime's plain-ESM dictionary synchronized with the richer TypeScript
// source package. The generated module ships inside signed artifacts and requires no runtime I/O.
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const root = path.resolve(import.meta.dirname, '..');
const sourcePath = path.join(root, 'games', 'word-wahala', 'source', 'dictionary.ts');
const targetPath = path.join(root, 'runtime', 'games', 'word-dictionary.js');
const source = await readFile(sourcePath, 'utf8');
const names = ['PIDGIN_WORDS', 'SLANG_WORDS', 'INDIGENOUS_WORDS', 'STANDARD_WORDS'];
const groups = {};

for (const name of names) {
  const match = source.match(new RegExp(`export const ${name}:[^=]+=[\\s]*\\[([\\s\\S]*?)\\n\\];`));
  if (!match) throw new Error(`Could not extract ${name} from ${sourcePath}`);
  // The array bodies are maintained source code containing only quoted strings and comments.
  groups[name] = Function(`"use strict"; return [${match[1]}];`)();
}

const tiers = Object.fromEntries([
  ...groups.STANDARD_WORDS.map((word) => [word.toUpperCase(), 'standard']),
  ...groups.PIDGIN_WORDS.map((word) => [word.toUpperCase(), 'pidgin']),
  ...groups.SLANG_WORDS.map((word) => [word.toUpperCase(), 'slang']),
  ...groups.INDIGENOUS_WORDS.map((word) => [word.toUpperCase(), 'indigenous']),
]);
const output = `// Generated by scripts/sync-word-dictionary.mjs from games/word-wahala/source/dictionary.ts.\n`
  + `// Do not edit this file directly.\n`
  + `export const WORD_TIERS = ${JSON.stringify(tiers, null, 2)};\n`
  + `export const WORD_DICTIONARY = Object.freeze(Object.keys(WORD_TIERS));\n`;
await writeFile(targetPath, output);
console.log(`Synced ${Object.keys(tiers).length} Word Wahala dictionary entries.`);
